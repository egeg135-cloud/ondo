// 방문자를 A/B 버킷에 1회 배정(localStorage 고정). 추후 실험 노출 추적용.
export function pickHeroVariant(): 'a' | 'b' {
  try {
    const saved = localStorage.getItem('ondo_hero_variant')
    if (saved === 'a' || saved === 'b') return saved
    const v = Math.random() < 0.5 ? 'a' : 'b'
    localStorage.setItem('ondo_hero_variant', v)
    return v
  } catch {
    return 'a'
  }
}

